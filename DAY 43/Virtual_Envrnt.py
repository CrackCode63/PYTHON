# What is Virtual Environment?
# Virtual Environment is a tool to create isolated Python environments.
# The basic problem being addressed is one of dependencies and versions, and indirectly permissions.
# Imagine you have an application that needs version 1 of LibFoo, but another application requires version 2 of LibFoo, and you need both applications to work on the same machine. Using virtualenv, you'd create two versions of the Python interpreter, one for each app. The first version of the interpreter would have version 1 of LibFoo, while the other would have version 2.
# The end result is that the apps are fully portable and don't depend on things that vary from system to system.


